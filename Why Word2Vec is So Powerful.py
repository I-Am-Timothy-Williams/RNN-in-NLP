#Why is Word 2 Vec so Powerful?

# Sentences with slightly different words that are similar enough can still be recognized as being similar because of the context
# so two similar sentences that have different color descriptors will still be viewed as being similar.
# You can create vectors from words that allow the computer to understand analogies such as Queen = King - Man + Woman
# Or in other words, King is to Man as Queen is to Woman